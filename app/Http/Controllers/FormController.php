    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $emp_agrement = EmploymentAgrementModel::findOrFail(1);
        $emp_validate = Validator::make($request->all(), $emp_agrement->validation());

        if ( $emp_validate->fails()) {
            return back()->withInput()->withErrors($emp_validate->errors());
        }
        
        $emp_agrement->fill($request->all())->save();
        return back()->with('message', 'Form Updated Successfully');
    }

